
#include <iostream>
using namespace std; 

int main()
{
   short int a;
   short int b;
 
    cout << "please enter the first number?" << endl;
    cin >> a;
    cout << "please enter the second number?" << endl;
    cin >> b;
    
   
    cout << a  << " + "  <<  b  << " \=\ " << a+b << endl;
    cout << a << " - " << b << " \=\ " << a - b << endl;
    cout << a << " * " << b << " \=\ " << a * b << endl;
    cout << a << " / " << b << " \=\ " << a / b << endl;
    cout << a << " % " << b << " \=\ " << a % b << endl;

}



